# Tipe Data Boolean
a = True
b = False
print(a)
print(b)
# Tipe Data String
c = "Teknik Pertanian dan Biosistem"
print(c)
# Tipe Data Integer
d = 10; e = 15
print(d); print(e)
# Tipe Data Float
f = 3.14
print(f)
# Tipe Data List
g = ["Python", 5.0, False]
print(g[0])
print(g[1])
print(g[2])
# Tipe Data Tuple
h = ("Python", 123, True)
print(h[0])
print(h[1])
print(h[2])
# Tipe Data Dictionary
i = { 1: "Belajar", 2: "Python"}
j = { "apa": "Belajar Python"}
print(i)
print(j)
# Cara memanggil salah satu elemen di dictionary
print(i[1])
print(i[2])
print(j["apa"])

